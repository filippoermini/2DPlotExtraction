package it.unifi.domain;

public class ValueInfo {

	private double[] values;
	private Bbox[] bboxes;
	
	public double[] getValues() {
		return values;
	}
	public void setValues(double[] values) {
		this.values = values;
	}
	public Bbox[] getBboxes() {
		return bboxes;
	}
	public void setBboxes(Bbox[] bboxes) {
		this.bboxes = bboxes;
	}
	
	
}
